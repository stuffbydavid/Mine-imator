			return IntType(0);
		sReal(block_state_id_current) = builder_get_state_id(self, sInt(build_pos_x), sInt(build_pos_y), sInt(build_pos_z));
		sInt(block_pos_x) = sInt(build_pos_x) * block_size_;
		sInt(block_pos_y) = sInt(build_pos_y) * block_size_;
