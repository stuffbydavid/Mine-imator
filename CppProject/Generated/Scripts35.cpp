			self->bounds_parts_end = point3D(-no_limit, -no_limit, -no_limit);
			self->has_3d_plane = false;
			VarType partlist = DsMap(map).Value(/*"parts"*/ STR(1846));
			self->render_part_list = ds_list_create();
