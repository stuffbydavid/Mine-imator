	VarType eval(VarType str, VarType def)
	{
		IntType i, values, ops;
		VarType result;
		StringType lastoperator;
		values = ds_stack_create();
		ops = ds_stack_create();
		lastoperator = /*""*/ STR(0);
		for (i = IntType(1); i < string_length(str) + IntType(1); i++)
		{
			StringType char_ = string_char_at(str, i);
			if (char_ == /*" "*/ STR(17))
				continue;
			else
				if (char_ == /*"("*/ STR(1074))
				{
					if (lastoperator == /*"--"*/ STR(1075))
						ds_stack_push({ ops, /*"-("*/ STR(1076) });
					else
						ds_stack_push({ ops, /*"("*/ STR(1074) });
					
					lastoperator = /*""*/ STR(0);
				}
				else
					if (eval_is_digit(char_))
					{
						RealType val, dec, decamount;
						BoolType deccheck;
						val = IntType(0);
						dec = IntType(0);
						deccheck = false;
						decamount = IntType(0);
						while (i < (string_length(str) + IntType(1)) && eval_is_digit(string_char_at(str, i)))
						{
							if (string_char_at(str, i) == /*"."*/ STR(1077))
							{
								deccheck = true;
								i++;
								continue;
							}
							if (deccheck)
							{
								dec = (dec * IntType(10)) + real(string_char_at(str, i));
								decamount++;
							}
							else
								val = (val * IntType(10)) + real(string_char_at(str, i));
							
							i++;
						}
						
						i--;
						if (decamount > IntType(0))
							dec /= power(10.0, decamount);
						if (lastoperator == /*"--"*/ STR(1075))
							ds_stack_push({ values, -(val + dec) });
						else
							ds_stack_push({ values, val + dec });
						
						lastoperator = /*""*/ STR(0);
					}
					else
						if (char_ == /*")"*/ STR(1078))
						{
							while (!ds_stack_empty(ops) && !(ds_stack_top(ops) == /*"("*/ STR(1074) || ds_stack_top(ops) == /*"-("*/ STR(1076)))
							{
								VarType val2, val1, op;
								val2 = ds_stack_top(values);
								ds_stack_pop(values);
								val1 = ds_stack_top(values);
								ds_stack_pop(values);
								op = ds_stack_top(ops);
								ds_stack_pop(ops);
								ds_stack_push({ values, eval_solve(val1, val2, op) });
							}
							
							if (!ds_stack_empty(ops))
							{
								if (ds_stack_top(ops) == /*"-("*/ STR(1076))
								{
									VarType val = -ds_stack_top(values);
									ds_stack_pop(values);
									ds_stack_push({ values, val });
								}
								ds_stack_pop(ops);
							}
							lastoperator = /*""*/ STR(0);
						}
						else
						{
							if (lastoperator != /*""*/ STR(0) && char_ == /*"-"*/ STR(1079))
							{
								lastoperator = /*"--"*/ STR(1075);
								continue;
							}
							while (!ds_stack_empty(ops) && eval_precedence(ds_stack_top(ops)) >= eval_precedence(char_))
							{
								VarType val2, val1, op;
								val2 = ds_stack_top(values);
								ds_stack_pop(values);
								val1 = ds_stack_top(values);
								ds_stack_pop(values);
								op = ds_stack_top(ops);
								ds_stack_pop(ops);
								ds_stack_push({ values, eval_solve(val1, val2, op) });
							}
							
							ds_stack_push({ ops, char_ });
							lastoperator = char_;
						}
					
				
			
			
		}
		while (!ds_stack_empty(ops))
		{
			VarType val2, val1, op;
			val2 = ds_stack_top(values);
			ds_stack_pop(values);
			val1 = ds_stack_top(values);
			ds_stack_pop(values);
			op = ds_stack_top(ops);
			ds_stack_pop(ops);
			ds_stack_push({ values, eval_solve(val1, val2, op) });
		}
		
		result = ds_stack_top(values);
		ds_stack_destroy(values);
		ds_stack_destroy(ops);
		if (result == VarType())
			return def;
		else
			return result;
		
		return VarType();
	}
	
	VarType eval_solve(RealType a, RealType b, StringType op)
	{
		if (a == VarType() && b != VarType() && op == /*"-"*/ STR(1079))
			a = IntType(0);
		if (a == VarType() || b == VarType() || op == VarType())
			return VarType();
		switch (StringType(op).id)
		{
			case 1080: // +
			{
				return a + b;
			}
			case 1079: // -
			{
				return a - b;
			}
			case 1081: // *
			{
				return a * b;
			}
			case 16: // /
			{
				return b != IntType(0) ? a / b : 0.0;
			}
			case 1082: // ^
			{
				return power(a, b);
			}
			case 204: // %
			{
				return (mod(a, b));
			}
		}
		
		return VarType();
	}
	
	BoolType eval_is_digit(StringType char_)
	{
		ArrType digits = ArrType::From({ /*"0"*/ STR(1036), /*"1"*/ STR(1083), /*"2"*/ STR(1084), /*"3"*/ STR(1085), /*"4"*/ STR(1086), /*"5"*/ STR(1087), /*"6"*/ STR(1088), /*"7"*/ STR(1089), /*"8"*/ STR(1090), /*"9"*/ STR(1091), /*"."*/ STR(1077) });
		for (IntType i = IntType(0); i < array_length(VarType::CreateRef(digits)); i++)
			if (char_ == digits.Value(i))
				return true;
		return false;
	}
	
	RealType eval_precedence(StringType op)
	{
		switch (StringType(op).id)
		{
			case 1079: // -
			case 1080: // +
			{
				return IntType(1);
			}
			case 16: // /
			case 1081: // *
			case 204: // %
			{
				return IntType(2);
			}
			case 1082: // ^
			{
				return IntType(3);
			}
		}
		
		return IntType(0);
	}
	
